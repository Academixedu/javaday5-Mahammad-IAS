package com.example;

/**
 * ArithmeticOperatorsExample
 */
public class ArithmeticOperatorsExample {

    public static void main(String[] args) {
        int a,b;
        a=2 ;
        b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a%b);
        System.out.println(a/b);
    }
}
